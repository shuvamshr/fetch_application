class Pet {
  final String id;
  final String name;
  final String breed;
  final String gender;
  final DateTime dateOfBirth;
  final bool deSexed;
  final String from;
  final String image;

  Pet({
    required this.id,
    required this.name,
    required this.breed,
    required this.gender,
    required this.dateOfBirth,
    required this.deSexed,
    required this.from,
    required this.image,
  });
}
